//C program to check whether the number is even or odd using if else
#include<stdio.h>
int main()
{
int num;
printf("enter the number\n");
scanf("%d",&num);
if (num%2==0)
{
    printf("number is EVEN\n");
}
else
{
    printf("number is ODD\n");
}
return 0;
}               //SuzanCodes:)